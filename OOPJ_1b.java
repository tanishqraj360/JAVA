public class OOPJ_1b {
    public static void main(String[] args) {
        System.out.println(Integer.parseInt(args[0]) + Integer.parseInt(args[1]));
        System.out.println(Integer.parseInt(args[0]) - Integer.parseInt(args[1]));
        System.out.println(Integer.parseInt(args[0]) * Integer.parseInt(args[1]));
        System.out.println(Integer.parseInt(args[0]) / Integer.parseInt(args[1]));
    }
}
